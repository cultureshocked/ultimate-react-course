const KEY = "3592dc3a";

export default function App() {
  const [movies, setMovies] = useState(tempMovieData);
  const [watched, setWatched] = useState(tempWatchedData);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  const [query, setQuery] = useState("");

  useEffect(
    function () {
      async function fetchMovieList() {
        setError("");
        if (query === "") return;
        try {
          setIsLoading(true);
          const res = await fetch(
            `http://www.omdbapi.com/?apikey=${KEY}&s=${query}`
          );
          if (!res.ok) {
            throw new Error("Something went wrong. Check your connection.");
          }
          const data = await res.json();

          if (data.Response === "False")
            throw new Error("No results for the provided search.");
          setMovies(data.Search);
        } catch (err) {
          console.log(err);
          setError(err.message);
        } finally {
          setIsLoading(false);
        }
      }
      if (query.length < 3) {
        setMovies([]);
        setError("");
        return;
      }
      fetchMovieList();
    },
    [query]
  );

  return (
    <>
      <NavBar>
        <Logo />
        <Search setQuery={setQuery} />
        <NumResults movies={movies} />
      </NavBar>
      <Main>
        <Box>
          {isLoading ? <Loader /> : null} {/* Loading*/}
          {!isLoading && !error ? <MovieList movies={movies} /> : null}{" "}
          {/* No load, no err*/}
          {error ? <ErrorMessage msg={error} /> : null} {/* err */}
        </Box>
        <Box>
          <WatchedSummary watched={watched} />
          <WatchedMoviesList watched={watched} />
        </Box>
      </Main>
    </>
  );
}
